-- FILE_EXTENSIONS 테이블 생성 DDL
CREATE TABLE FILE_EXTENSIONS (
  EXT_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  EXT_TYPE VARCHAR2(10) NOT NULL,  -- 'FIXED' or 'CUSTOM'
  EXT_NAME VARCHAR2(20) NOT NULL,
  IS_BLOCKED CHAR(1) DEFAULT 'N' CHECK (IS_BLOCKED IN ('Y','N')),
  CREATED_AT DATE DEFAULT SYSDATE
);

CREATE UNIQUE INDEX UX_FILE_EXTENSIONS_TYPE_NAME ON FILE_EXTENSIONS (EXT_TYPE, LOWER(EXT_NAME));